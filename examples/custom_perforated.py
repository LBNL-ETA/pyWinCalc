# Path to the optical standard file.  All other files referenced by the standard file must be in the same directory
# Note:  While all optical standards packaged with WINDOW should work with optical calculations care should be
# taken to use NFRC standards if NFRC thermal results are desired.  This is because for thermal calculations currently
# only ISO 15099 is supported.  While it is possible to use EN optical standards and create thermal results
# those results will not be based on EN 673
optical_standard_path = "standards/W5_NFRC_2003.std"
optical_standard = pywincalc.load_standard(optical_standard_path)

glazing_system_width = 1.0  # width of the glazing system in meters
glazing_system_height = 1.0  # height of the glazing system in meters

# Define the gap between the shade and the glazing
gap_1 = pywincalc.Gap(pywincalc.PredefinedGasType.AIR, .0127)  # .0127 is gap thickness in meters

# A woven shade requires a BSDF hemisphere.  Create one based on a standard quarter basis for this test
bsdf_hemisphere = pywincalc.BSDFHemisphere.create(pywincalc.BSDFBasisType.QUARTER)

# Create optical data for the glass layer

glass_material_type = pywincalc.MaterialType.MONOLITHIC
glass_material_thickness = .003048  # 3.048mm thick
glass_wavelength_measurements = convert_wavelength_data(raw_glazing_wavelength_data())
# Since the measurements do not extend to the IR range emissivity and IR transmittances should be provided
glass_emissivity_front = .84
glass_emissivity_back = .84
glass_ir_transmittance_front = 0
glass_ir_transmittance_back = 0
glass_coated_side = pywincalc.CoatedSide.NEITHER
glass_permeability_factor = 0
flipped = False

glass_n_band_optical_data = pywincalc.ProductDataOpticalNBand(material_type=glass_material_type,
                                                              thickness_meters=glass_material_thickness,
                                                              wavelength_data=glass_wavelength_measurements,
                                                              coated_side=glass_coated_side,
                                                              ir_transmittance_front=glass_ir_transmittance_front,
                                                              ir_transmittance_back=glass_ir_transmittance_back,
                                                              emissivity_front=glass_emissivity_front,
                                                              emissivity_back=glass_emissivity_back,
                                                              permeability_factor=glass_permeability_factor,
                                                              flipped=flipped)

# Next create the thermal data for the glass layer
glass_conductivity = 1
# Since thermal openings in this case are all zero they can be omitted.  They are included he for example purposes.
glass_opening_top = 0
glass_opening_bottom = 0
glass_opening_left = 0
glass_opening_right = 0
glass_opening_front = 0

glass_thermal = pywincalc.ProductDataThermal(conductivity=glass_conductivity,
                                             thickness_meters=glass_material_thickness,
                                             flipped=flipped,
                                             opening_top=glass_opening_top,
                                             opening_bottom=glass_opening_bottom,
                                             opening_left=glass_opening_left,
                                             opening_right=glass_opening_right,
                                             opening_front=glass_opening_front)

# Create a glass layer from both the optical and thermal data
glass_layer = pywincalc.ProductDataOpticalAndThermal(glass_n_band_optical_data, glass_thermal)

# Next start on the shade layer.  Create n-band optical product data for the shade material
# n-band is created because per-wavelength measurements are available
# single and dual band products are not yet supported
shade_material_type = pywincalc.MaterialType.MONOLITHIC
shade_material_thickness = .001  # 1 mm thick
shade_material_wavelength_measurements = convert_wavelength_data(raw_shade_material_wavelength_data())
# Since the measurements do not extend to the IR range emissivity and IR transmittances should be provided
# These numbers are just for example
shade_emissivity_front = .8
shade_emissivity_back = .76
shade_ir_transmittance_front = .02
shade_ir_transmittance_back = .01
shade_permeability_factor = 0

flipped = False

shade_material_n_band_optical_data = pywincalc.ProductDataOpticalNBand(material_type=shade_material_type,
                                                                       thickness_meters=shade_material_thickness,
                                                                       wavelength_data=shade_material_wavelength_measurements,
                                                                       ir_transmittance_front=shade_ir_transmittance_front,
                                                                       ir_transmittance_back=shade_ir_transmittance_back,
                                                                       emissivity_front=shade_emissivity_front,
                                                                       emissivity_back=shade_emissivity_back,
                                                                       permeability_factor=shade_permeability_factor,
                                                                       flipped=flipped)

# Create perforated geometry 
perforation_type = pywincalc.PerforatedGeometry.Type.RECTANGULAR
spacing_x = .02  # 20mm horizontal spacing
spacing_y = .03  # 30mm vertical spacing
dimension_x = .001  # 1mm perforation in the horizontal direction
dimension_y = .003  # 3mm perforation in the vertical direction

geometry = pywincalc.PerforatedGeometry(spacing_x, spacing_y, dimension_x, dimension_y, perforation_type)

# Create a ProductDataOpticalPerforatedScreen from optical data and geometry
perforated_screen_optical = pywincalc.ProductDataOpticalPerforatedScreen(
    material_product_data_optical=shade_material_n_band_optical_data,
    geometry=geometry)

# Create a pywincalc.ProductDataThermal out of the information that only applies to thermal calculations
# These numbers are only for example purposes.
shade_conductivity = 160
shade_opening_top = .01
shade_opening_bottom = .01
shade_opening_left = .02
shade_opening_right = .02

perforated_screen_thermal = pywincalc.ProductDataThermal(conductivity=shade_conductivity,
                                                         thickness_meters=shade_material_thickness,
                                                         flipped=flipped,
                                                         opening_top=shade_opening_top,
                                                         opening_bottom=shade_opening_bottom,
                                                         opening_left=shade_opening_left,
                                                         opening_right=shade_opening_right)

# Combine optical and thermal parts into one pywincalc.Product_Data_Optical_Thermal
perforated_layer = pywincalc.ProductDataOpticalAndThermal(perforated_screen_optical,
                                                          perforated_screen_thermal)

# Create a glazing system using the NFRC U environment in order to get NFRC U results
# U and SHGC can be caculated for any given environment but in order to get results
# The NFRC U and SHGC environments are provided as already constructed environments and Glazing_System
# defaults to using the NFRC U environments
glazing_system_u_environment = pywincalc.GlazingSystem(optical_standard=optical_standard,
                                                       solid_layers=[perforated_layer, glass_layer],
                                                       gap_layers=[gap_1],
                                                       width_meters=glazing_system_width,
                                                       height_meters=glazing_system_height,
                                                       environment=pywincalc.nfrc_u_environments(),
                                                       bsdf_hemisphere=bsdf_hemisphere)

# In order to get NFRC SHGC results the NFRC SHGC environment should be used when creating the glazing system
glazing_system_shgc_environment = pywincalc.GlazingSystem(optical_standard=optical_standard,
                                                          solid_layers=[perforated_layer, glass_layer],
                                                          gap_layers=[gap_1],
                                                          width_meters=glazing_system_width,
                                                          height_meters=glazing_system_height,
                                                          environment=pywincalc.nfrc_shgc_environments(),
                                                          bsdf_hemisphere=bsdf_hemisphere)

results_name = "Results for a double-layer system with a custom exterior perforated screen made from user-defined spectral data."
print("*" * len(results_name))
print(results_name)
print("*" * len(results_name))
results_printer.print_results(glazing_system_u_environment, glazing_system_shgc_environment)
